def do_curl(csv)
  csv.each_line do |line|
    split = line.strip.split(", ")
    if split.length > 1
      url = split[1].strip
      file_name = url.split("/").last
      output_name = RAILS_ROOT + "/public/system/photos/#{split[0]}/original/#{file_name}"
      `curl -o #{output_name} #{url} --create-dirs`
    end
  end
end

def do_paperclip_import(csv)
  csv.each_line do |line|
    split = line.strip.split(", ")

    if split.length > 1
      url = split[1].strip
      file_name = url.split("/").last
      output_name = RAILS_ROOT + "/public/system/photos/#{split[0]}/original/#{file_name}"
      p = Production.find(split[0].to_i)
      p.photo = File.new(output_name)
      p.save!
    end
  end
end

